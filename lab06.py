
def make_adder_inc(n):
    """
    >>> adder1 = make_adder_inc(5)
    >>> adder2 = make_adder_inc(6)
    >>> adder1(2) 
    7
    >>> adder1(2) # 5 + 2 + 1
    8
    >>> adder1(10) # 5 + 10 + 2
    17
    >>> [adder1(x) for x in [1, 2, 3]]
    [9, 11, 13]
    >>> adder2(5)
    11
    """
    "*** YOUR CODE HERE ***"
    inc = 0
    
    def adder(x):
        nonlocal inc
        ls = [n]
        ls.append(x)
        ls.append(inc)
        inc += 1
        print("DEBUG:", ls)
        return sum(ls)

    return adder